SELECT VERSIONS_OPERATION AS op, COUNT(*) AS cnt
FROM   your_schema.your_table
       VERSIONS BETWEEN TIMESTAMP
         TO_TIMESTAMP_TZ('2025-10-21 00:00:00 +03:00','YYYY-MM-DD HH24:MI:SS TZH:TZM')
     AND TO_TIMESTAMP_TZ('2025-10-22 23:59:59 +03:00','YYYY-MM-DD HH24:MI:SS TZH:TZM')
GROUP  BY VERSIONS_OPERATION;
